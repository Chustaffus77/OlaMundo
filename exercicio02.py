#Fazer um programa que pergunte um número inteiro e apresente ao usuário o antecessor e o sucessor do número informado.

nome = input("Qual é o seu nome?")

num = int(input("Insira um número inteiro:"))

antecessor = num - 1

sucessor = num + 1

print("O número",num, "tem como antecessor o nº",antecessor)
print("Já o sucessor dele é o nº",sucessor)


print("Espero ter ajudado,",nome,"!Volte Sempre!")